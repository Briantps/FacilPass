package constant;

public enum ObjectionStateType {
	
	PENDING_AUTHORIZATION(0L),
	AUTHORIZED(1L),
	REJECTED(2L),
	ADJUSTMENT_APPLIED(3L);
	
	private Long stateObjection;

	private ObjectionStateType(Long str) {
		this.stateObjection = str;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Enum#toString()
	 */
	public Long getId() {
		return this.stateObjection;
	}
}

package constant;

public enum AccountStateType {
	
	ACTIVE(0L),
	INACTIVE(1L),
	PENDING_FOR_CLOSURE(2L),
	TO_CLOSE(3L),
	CLOSE(4L),
	SUSPEND(5L),
	PENDING_FOR_FIRST_RECHARGE(7L);
	
	private Long id;

	 /**
	  * 
	  * @param id
	  */
	 private AccountStateType(Long id) {
	   this.id = id;
	 }

	 /**
	  * 
	  * @return id
	  */
	 public Long getId() {
	  	 return id;
	 }
}

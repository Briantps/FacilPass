package util;

import java.io.Serializable;

import jpa.TbPurchaseOrderDetail;

/**
 * @author tmolina
 *
 */
public class OrderDetail implements Serializable {
	private static final long serialVersionUID = -1373119030121812950L;
	
	private TbPurchaseOrderDetail detail;

	private Long idStation; 
	
	private Long idDevice;
	
	private Long valueTo;
	
	/**
	 * Constructor
	 */
	public OrderDetail() {
	}

	/**
	 * @param detail the detail to set
	 */
	public void setDetail(TbPurchaseOrderDetail detail) {
		this.detail = detail;
	}

	/**
	 * @return the detail
	 */
	public TbPurchaseOrderDetail getDetail() {
		return detail;
	}

	/**
	 * @param idStation the idStation to set
	 */
	public void setIdStation(Long idStation) {
		this.idStation = idStation;
	}

	/**
	 * @return the idStation
	 */
	public Long getIdStation() {
		return idStation;
	}

	/**
	 * @param idDevice the idDevice to set
	 */
	public void setIdDevice(Long idDevice) {
		this.idDevice = idDevice;
	}

	/**
	 * @return the idDevice
	 */
	public Long getIdDevice() {
		return idDevice;
	}

	/**
	 * @param valueTo the valueTo to set
	 */
	public void setValueTo(Long valueTo) {
		this.valueTo = valueTo;
	}

	/**
	 * @return the valueTo
	 */
	public Long getValueTo() {
		return valueTo;
	}
}
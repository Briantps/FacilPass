package constant;

/**
 * Enum for Roles.
 * 
 * @author tmolina
 * @since 15-02-2011
 */
public enum Role {
	
	SUPERUSER(1),
	PREENROLLED(2),
	CLIENT(3),
	INSTALLER(4),
	INACTIVEUSER(5);

	 private Integer id;

	 /**
	  * 
	  * @param id
	  */
	 private Role(Integer id) {
	   this.id = id;
	 }

	 /**
	  * 
	  * @return id
	  */
	 public Integer getId() {
	  	 return id;
	 }
}
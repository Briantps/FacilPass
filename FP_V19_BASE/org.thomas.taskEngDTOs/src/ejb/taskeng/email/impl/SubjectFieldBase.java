package ejb.taskeng.email.impl;

import ejb.taskeng.email.SubjectField;

public class SubjectFieldBase implements SubjectField {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private int id;
	private String content;
	
	public SubjectFieldBase(){
	}
	
	/* (non-Javadoc)
	 * @see email.SubjectField#setId(int)
	 */
	public void setId(int id) {
		this.id = id;
	}

	/* (non-Javadoc)
	 * @see email.SubjectField#getId()
	 */
	public int getId() {
		return id;
	}

	/* (non-Javadoc)
	 * @see email.SubjectField#setContent(java.lang.String)
	 */
	public void setContent(String content) {
		this.content = content;
	}

	/* (non-Javadoc)
	 * @see email.SubjectField#getContent()
	 */
	public String getContent() {
		return content;
	}
	
	
}

package jpa;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name="TB_EMAIL_PARAMETERS")
public class TbEmailParameters implements Serializable {
	private static final long serialVersionUID = 1L;
	
	@Id
	@Column(name="EMAIL_PARAMETERS_ID")
	private Long emailParametersId;
	
	@Column(name="EMAIL_PARAMETERS_ABBREVIATION")
	private String emailParametersAbbreviation;
	
	@Column(name="EMAIL_PARAMETERS_NAME")
	private String emailParametersName;
	
	@Column(name="EMAIL_PARAMETERS_REPLACE")
	private String emailParametersReplace;
	
	@Column(name="EMAIL_PARAMETERS_TABLE_REF")
	private String emailParametersTableRef;
	
	@Column(name="EMAIL_PARAMETERS_FIELD_REF")
	private String emailParametersFieldRef;
	
	@Column(name="EMAIL_PARAMETERS_DESCRIPTION")
	private String emailParametersDescription;
	
	@Column(name="EMAIL_PARAMETERS_WHERE")
	private String emailParametersWhere;
	
	@Column(name="EMAIL_PARAMETERS_INPUT")
	private String emailParametersInput;
	
	@Column(name="EMAIL_PARAMETERS_DEMO")
	private String emailParametersDemo;

	public void setEmailParametersId(Long emailParametersId) {
		this.emailParametersId = emailParametersId;
	}

	public Long getEmailParametersId() {
		return emailParametersId;
	}

	public void setEmailParametersAbbreviation(
			String emailParametersAbbreviation) {
		this.emailParametersAbbreviation = emailParametersAbbreviation;
	}

	public String getEmailParametersAbbreviation() {
		return emailParametersAbbreviation;
	}

	public void setEmailParametersName(String emailParametersName) {
		this.emailParametersName = emailParametersName;
	}

	public String getEmailParametersName() {
		return emailParametersName;
	}

	public void setEmailParametersReplace(String emailParametersReplace) {
		this.emailParametersReplace = emailParametersReplace;
	}

	public String getEmailParametersReplace() {
		return emailParametersReplace;
	}

	public void setEmailParametersTableRef(String emailParametersTableRef) {
		this.emailParametersTableRef = emailParametersTableRef;
	}

	public String getEmailParametersTableRef() {
		return emailParametersTableRef;
	}

	public void setEmailParametersFieldRef(String emailParametersFieldRef) {
		this.emailParametersFieldRef = emailParametersFieldRef;
	}

	public String getEmailParametersFieldRef() {
		return emailParametersFieldRef;
	}

	public void setEmailParametersDescription(String emailParametersDescription) {
		this.emailParametersDescription = emailParametersDescription;
	}

	public String getEmailParametersDescription() {
		return emailParametersDescription;
	}

	public void setEmailParametersWhere(String emailParametersWhere) {
		this.emailParametersWhere = emailParametersWhere;
	}

	public String getEmailParametersWhere() {
		return emailParametersWhere;
	}

	public void setEmailParametersInput(String emailParametersInput) {
		this.emailParametersInput = emailParametersInput;
	}

	public String getEmailParametersInput() {
		return emailParametersInput;
	}

	public void setEmailParametersDemo(String emailParametersDemo) {
		this.emailParametersDemo = emailParametersDemo;
	}

	public String getEmailParametersDemo() {
		return emailParametersDemo;
	}
}
